            92A &mdash; Automated Logistical Specialist
          </h3>
          <p class="text-muted-foreground leading-relaxed mb-4">
            The 92A is the direct pipeline into LOGCAP supply chain positions.
            These specialists manage property accountability, supply
            distribution, and warehouse operations at scale. Pay ranges from
            $60K&ndash;$100K, which is lower than combat or intelligence roles
            but comes with several advantages: lower clearance requirements
            (Secret is typically sufficient), high volume of available
            positions, and established 12-month rotation schedules with
            predictable leave cycles. For veterans looking for steady,
            long-term contractor employment rather than high-risk premium
            pay, 92A positions offer excellent stability. Understanding the
