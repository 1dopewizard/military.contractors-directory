              class="text-primary hover:underline"
              >tax advantages of OCONUS contractor work</NuxtLink
            >
            can make these salaries significantly more competitive.
          </p>

          <h3 class="text-foreground text-xl font-semibold mt-8 mb-3">
            51C &mdash; Acquisition, Logistics &amp; Technology
          </h3>
          <p class="text-muted-foreground leading-relaxed mb-4">
            The 51C bridges the gap between operational logistics and
            acquisition management. These professionals manage contracts,
            coordinate with defense acquisition agencies, and oversee technology
            integration at the program level. Contractor positions pay
            $80K&ndash;$130K, with the higher end reserved for those with DAWIA
            (Defense Acquisition Workforce Improvement Act) certifications and
            PMP credentials. This MOS transitions well into program management
            and acquisition support roles with the major primes.
          </p>

          <h3 class="text-foreground text-xl font-semibold mt-8 mb-3">
            91-Series &mdash; Maintenance
          </h3>
          <p class="text-muted-foreground leading-relaxed mb-4">
            The 91-series covers vehicle, aircraft, and weapons system
