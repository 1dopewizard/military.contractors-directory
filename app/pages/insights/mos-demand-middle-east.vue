          <h2 class="text-foreground text-2xl font-bold mt-12 mb-4">
            4. Combat Arms &mdash; The Security Contractor Pipeline
          </h2>
          <p class="text-muted-foreground leading-relaxed mb-4">
            Combat arms MOSs form the backbone of the security contracting
            pipeline. While they do not command the same premiums as SOF or
            intelligence specialties, the sheer volume of available positions
            makes combat arms one of the most accessible entry points into
            defense contracting. The key differentiator is deployment
            experience: a 11B with two combat deployments and a Secret clearance
            will be placed far faster than one without.
          </p>

          <h3 class="text-foreground text-xl font-semibold mt-8 mb-3">
            11B &mdash; Infantryman
          </h3>
          <p class="text-muted-foreground leading-relaxed mb-4">
            The 11B is the single most common MOS transitioning into security
            contracting. Roles include static security, convoy escort, PSD
            (Personal Security Detail), and quick reaction force positions.
            Compensation ranges from $110K&ndash;$200K, with the spread driven
            almost entirely by location and threat level. A static security
            position at a relatively low-threat installation in Qatar might pay
            $110K, while a PSD operator in a high-threat environment commands
            $180K+. Combat experience is the premium: multiple deployments,
            CIB-holders, and those with vehicle commander or squad leader
            experience are placed first. Contractors like Constellis, Triple
            Canopy (now part of Constellis), GardaWorld, and SOC LLC run
            continuous hiring for qualified 11Bs.
          </p>

          <h3 class="text-foreground text-xl font-semibold mt-8 mb-3">
            0311 &mdash; USMC Rifleman
          </h3>
          <p class="text-muted-foreground leading-relaxed mb-4">
            The Marine equivalent of the 11B follows an identical contracting
            pipeline. Marine infantry veterans are highly regarded for their
            discipline and expeditionary mindset. Compensation and role
            availability mirror the 11B market, with the Marine Corps' emphasis
            on small-unit leadership giving 0311s with fire team and squad
            leader experience a slight edge in PSD team selection.
          </p>

          <h3 class="text-foreground text-xl font-semibold mt-8 mb-3">
            19D &mdash; Cavalry Scout
          </h3>
          <p class="text-muted-foreground leading-relaxed mb-4">
            Cavalry Scouts bring reconnaissance and surveillance skills that
            translate directly into route clearance, advance party, and threat
            assessment roles in the contractor space. Their training in
            observation, reporting, and vehicle-based operations makes them
            particularly valuable for convoy security and mobile patrol
            contracts. Pay ranges are comparable to 11B positions, with a
            modest premium for those with specialized ISR (Intelligence,
            Surveillance, and Reconnaissance) platform experience.
          </p>

