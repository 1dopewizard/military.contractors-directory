        <h2 class="text-foreground text-2xl font-bold mt-12 mb-4">
          LOGCAP V -- The $82 Billion Pipeline
        </h2>

        <p class="text-muted-foreground leading-relaxed mb-4">
          The Logistics Civil Augmentation Program (LOGCAP V) is the single largest
          vehicle for deploying civilian contractors to support military operations,
          and it is the first contract mechanism that scales up when a new conflict
          begins. LOGCAP V is an $82 billion indefinite-delivery/indefinite-quantity
          (IDIQ) contract shared among four prime contractors.
        </p>

        <h3 class="text-foreground text-xl font-semibold mt-8 mb-3">
          LOGCAP V Prime Contractors
        </h3>

        <ul class="text-muted-foreground space-y-2 mb-6 ml-6 list-disc">
          <li><strong class="text-foreground">KBR</strong> -- The historical LOGCAP incumbent and largest single employer of overseas contractors. KBR held LOGCAP III during Iraq and Afghanistan and remains a dominant force under LOGCAP V.</li>
          <li><strong class="text-foreground">V2X (formerly Vectrus)</strong> -- Specializes in base operations, vehicle maintenance, and communications infrastructure. Strong presence in the Middle East and Africa.</li>
          <li><strong class="text-foreground">Fluor Corporation</strong> -- Major construction and engineering firm with defense logistics capabilities. Provides facility maintenance, construction, and environmental services.</li>
          <li><strong class="text-foreground">PAE-Parsons JV</strong> -- Joint venture combining PAE's operational expertise with Parsons' technical capabilities. Focuses on contingency operations and base life support.</li>
        </ul>

        <h3 class="text-foreground text-xl font-semibold mt-8 mb-3">
          What LOGCAP Covers
        </h3>

        <p class="text-muted-foreground leading-relaxed mb-4">
          LOGCAP provides comprehensive base operations support anywhere the US
          military deploys. This includes dining facilities, laundry services, power
          generation, water purification, waste management, vehicle and equipment
          maintenance, mail services, morale/welfare/recreation, billeting, and
          general facility maintenance. If there is a forward operating base, there
          are LOGCAP contractors running the infrastructure.
        </p>

        <p class="text-muted-foreground leading-relaxed mb-4">
          Pay ranges under LOGCAP V vary significantly by role and location. Entry-level positions such as food service and laundry typically start at $17-$22 per hour, while skilled trades (electricians, HVAC technicians, heavy equipment operators) command $35-$50 per hour. Supervisory and management roles can reach $55-$62 per hour. The typical deployed schedule is 12 hours per day, 7 days per week -- meaning the overtime alone can double your effective annual compensation. Combined with tax-free status in combat zones and the Foreign Earned Income Exclusion, LOGCAP positions remain one of the most accessible high-earning opportunities for veterans with trade skills.
        </p>

